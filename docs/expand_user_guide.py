def join_paragraphs(paragraphs: list[str]) -> str:
    return "\n\n".join(p.strip() for p in paragraphs if p.strip())


def describe_location(label: str, tab: str) -> str:
    if tab == "案件タブ":
        meta_controls = {
            "受付日",
            "会社名",
            "サポート番号",
            "ステータス",
            "ステータス追加（＋）",
            "ステータス削除（－）",
            "保存先カテゴリ",
            "作成フォルダ（プレビュー）",
            "新規ボタン",
            "フォルダ作成ボタン",
            "フォルダを開くボタン",
            "ステータス更新ボタン",
        }
        top_controls = {
            "ベースフォルダ欄",
            "参照ボタン",
            "履歴ドロップダウン",
            "履歴更新ボタン",
            "サポート番号検索欄",
            "検索ボタン",
        }
        note_controls = {
            "ノート種別",
            "テンプレート",
            "テンプレート保存",
            "テンプレートインポート",
            "テンプレートエクスポート",
            "テンプレート並べ替え",
            "ノートファイル表示",
            "ノート本文入力欄",
            "プレビュー",
            "ノートを開く",
            "追記保存",
            "クリア",
            "サブフォルダ作成",
            "本文プレビューダブルクリック",
        }
        checkbox_controls = {"作成後にフォルダを開く", "ダークモード"}
        if label in meta_controls:
            return "案件タブの「案件メタ情報」グループ内"
        if label in top_controls:
            return "案件タブ上部（ベースフォルダ / 履歴 / 検索のエリア）"
        if label in note_controls:
            return "案件タブ下部の「ノート編集」エリア内"
        if label in checkbox_controls:
            return "案件タブ中段のチェックボックス列"
        return "案件タブ内"

    if tab == "ステータスタブ":
        top_controls = {"再読み込み", "プロダクト状況"}
        open_controls = {
            "進行中一覧",
            "進行中一覧のステータス変更",
            "進行中一覧の開く",
            "進行中一覧の除外",
        }
        stale_controls = {
            "更新停滞一覧",
            "更新停滞の開く",
            "更新停滞の除外",
        }
        exclude_controls = {"除外リスト", "除外リストの戻す"}
        if label in top_controls:
            return "ステータスタブ上部"
        if label in open_controls:
            return "ステータスタブ中段の「進行中（未クローズ）」一覧内"
        if label in stale_controls:
            return "ステータスタブ下段の「更新停滞」一覧内"
